export const getExpensesByCategory = (category: string): Expense[] => {
  return expenses.filter((e) => e.category === category);
};

export const getExpensesByDateRange = (
  startDate: string,
  endDate: string
): Expense[] => {
  return expenses.filter((e) => e.date >= startDate && e.date <= endDate);
};

export const getExpensesByMonth = (month: string): Expense[] => {
  return expenses.filter((e) => e.date.startsWith(month));
};

export const getTotalExpensesByMonth = (month: string): number => {
  return getExpensesByMonth(month).reduce((sum, e) => sum + e.amount, 0);
};

export const getExpenseCategoryTotals = () => {
  const totals: { [key: string]: number } = {};
  expenses.forEach((e) => {
    totals[e.category] = (totals[e.category] || 0) + e.amount;
  });
  return totals;
};

export const getMonthlyExpenseTrend = () => {
  const months = [
    "2023-10",
    "2023-11",
    "2023-12",
    "2024-01",
    "2024-02",
    "2024-03",
  ];
  return months.map((month) => ({
    month,
    total: getTotalExpensesByMonth(month),
  }));
};
