export const beds: Bed[] = rooms.flatMap((room) => {
  const bedList: Bed[] = [];
  for (let i = 1; i <= room.capacity; i++) {
    bedList.push({
      id: `${room.id}-B${i}`,
      roomId: room.id,
      bedNumber: i,
      status: i <= room.occupiedBeds ? "occupied" : "vacant",
    });
  }
  return bedList;
});

export const getRoomById = (id: string): Room | undefined => {
  return rooms.find((r) => r.id === id);
};

export const getAvailableRooms = (): Room[] => {
  return rooms.filter((r) => r.status === "available");
};

export const getRoomsByFloor = (floor: number): Room[] => {
  return rooms.filter((r) => r.floor === floor);
};

export const getOccupancyStats = () => {
  const totalBeds = rooms.reduce((sum, r) => sum + r.capacity, 0);
  const occupiedBeds = rooms.reduce((sum, r) => sum + r.occupiedBeds, 0);
  return {
    totalRooms: rooms.length,
    totalBeds,
    occupiedBeds,
    vacantBeds: totalBeds - occupiedBeds,
    occupancyRate: Math.round((occupiedBeds / totalBeds) * 100),
  };
};
